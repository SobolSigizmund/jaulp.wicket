package de.alpharogroup.wicket.base.util.template;

/**
 * The Enum StringTextType can tag a {@link StringTextValue} with a type.
 */
public enum StringTextType
{
	/** The boolean. */
	BOOLEAN,
	/** The string. */
	STRING,
	/** The enum. */
	ENUM,
	/** The array. */
	ARRAY,
	/** The integer. */
	INTEGER,
	/** The float. */
	FLOAT,
	/** The string integer. */
	STRING_INTEGER;
}
